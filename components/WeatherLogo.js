export default function WeatherLogo(props) {
  const [weatherType, setWeatherType] = useState("sunny");

  useEffect(() => {
    axios
      .get(
        `https://api.seniverse.com/v3/weather/now.json?key=SjPJO8uWbDNjYyU4i&location=${props.city}&language=zh-Hans&unit=c`
      )
      .then((res) => {
        //将获取数据渲染
        const weatherText = res.data.results[0].now.text;
        switch (weatherText) {
          case "晴":
            setWeatherType("sunny");
            break;
          case "多云":
            setWeatherType("duocloudy");
            break;
          // add more cases for different weather types here
          case "阴":
            setWeatherType("cloudy");
            break;
          case "小雨":
            setWeatherType("rain");
            break;
            case "冰雹":
            setWeatherType("hail");
            break;
            case "雷雨":
            setWeatherType("thunderstorm");
            break;
            case "大雨":
            setWeatherType("heavyrain");
            break;
            case "中雨":
            setWeatherType("middlerain");
            break;
            case "暴雨":
            setWeatherType("rainstorm");
            break;
            case "沙尘":
            setWeatherType("sandstorm");
            break;
            case "小雪":
            setWeatherType("snow");
            case "中雪":
            setWeatherType("middlesnow");
            break;
          default:
            setWeatherType("");
            break;
        }
      });
  }, [props.city]);

  return <div>{weatherSvg[weatherType]}</div>;
}
